    {
        return score switch
        {
            >= 80 => "ğŸ¯ Promising - Strong potential!",
            >= 60 => "ğŸ”µ Moderate - Good foundation",
            >= 40 => "âš ï¸ Needs Work - Significant challenges",
            _ => "ğŸš« High Risk - Major concerns"
        };
    }
}
